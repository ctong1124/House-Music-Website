              <p>
                The Summer of Love began in 1988 with clubs like Shoom, Spectrum and Trip 
                but quickly expanded to illegal warehouse parties when the clubs became too full.
                The parties could not be advertised so party-goers would find the locations by
                word of mouth and the then-new mobile phones, often wandering London until they 
                reached the music.
              </p>
              <p> 
                Acid (LSD) soon joined the arsenel of party-goers and
                massive outdoor parties would spring up for a night in 
                villages outside cities. More than 10,000 teenagers would 
                descend upon an open field and party all weekend fueled by 
                alcohol, acid and ecstasy. As acid house and the drug-fueled 
                parties took over England's youth the press and government 
                began to report the wild antics. The parties continued, but 
                the scene's clubs and events would face strong police opposition. 
              </p>
